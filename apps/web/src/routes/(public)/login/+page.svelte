<script lang="ts">
  import { goto } from '$app/navigation';
  import { authStore } from '$lib/stores/auth.svelte';
  import { apiClient } from '$lib/api';
  import { invalidateAll } from '$app/navigation';

  // Estado local del formulario usando Runes
  let email = $state('');
  let password = $state('');
  let error = $state<string | null>(null);
  let isLoading = $state(false);

  async function handleLogin() {
    error = null;
    isLoading = true;

    try {
      // 1. Login: obtener token
      const response = await fetch('/api/auth/login', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ email, password })
      });

      // if (!response.ok) {
      //   const errorData = await response.json();
      //   throw new Error(errorData.message || 'Error al iniciar sesión');
      // }

      const data = await response.json();
      console.log('Login response (desde el login):', data);

      if (!response.ok) {
        throw new Error(data.message || 'Error al iniciar sesión');
      };


      // 2. Obtener perfil de usuario (la cookie httpOnly ya está seteada por el backend)
      const userRes = await fetch('/api/profile', {
        method: 'GET',
        credentials: 'include'
      });
      console.log('Respuesta del perfil de usuario (desde el login):', userRes);
      if (!userRes.ok) {
        throw new Error('No se pudo obtener el perfil de usuario (desde el login)');
      }
      const user = await userRes.json();

      // 3. Actualizar authStore (no guardamos token en frontend)
      authStore.set({ user });

      await invalidateAll();
      goto('/dashboard');
    } catch (e: any) {
      error = e.message;
    } finally {
      isLoading = false;
    }
  }
</script>

<div class="flex items-center justify-center min-h-screen bg-gray-100">
  <div class="p-8 bg-white rounded-lg shadow-md w-full max-w-md">
    <h1 class="text-2xl font-bold mb-6 text-center">Iniciar Sesión</h1>
    <form onsubmit={handleLogin}>
      <div class="mb-4">
        <label for="email" class="block text-gray-700 mb-2">Email</label>
        <input 
          type="email" 
          id="email" 
          bind:value={email}
          class="w-full px-3 py-2 border rounded-lg focus:outline-none focus:ring-2 focus:ring-blue-500"
          required
        />
      </div>
      <div class="mb-6">
        <label for="password" class="block text-gray-700 mb-2">Contraseña</label>
        <input 
          type="password" 
          id="password"
          bind:value={password}
          class="w-full px-3 py-2 border rounded-lg focus:outline-none focus:ring-2 focus:ring-blue-500"
          required
        />
      </div>

      {#if error}
        <p class="text-red-500 text-sm mb-4">{error}</p>
      {/if}

      <button 
        type="submit" 
        class="w-full bg-blue-600 text-white py-2 rounded-lg hover:bg-blue-700 transition duration-200"
        disabled={isLoading}
      >
        {isLoading ? 'Cargando...' : 'Entrar'}
      </button>
    </form>
  </div>
</div>